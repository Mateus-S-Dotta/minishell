#include "header.h"

int ft_env(char **env)
{
	int		i;
	int		j;
	
	i = -1;
	while (env[++i])
	{
		j = -1;
	 if (ft_strchr(env[i], '='))
	 {
		 while (env[i][++j])
		 {
			 if (env[i][j] != '\"')
			 write (1, &env[i][j], 1);
			}
		}
	}
	return (0);
}