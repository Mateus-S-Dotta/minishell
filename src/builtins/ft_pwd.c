#include "../../minishell.h"

int	ft_pwd(void)
{
	char	cwd[256];

	if (getcwd(cwd, 256))
	{
		ft_putstr_fd(cwd, 1);
		write(1, "\n", 1);
		return (0);
	}
	return (2);
}