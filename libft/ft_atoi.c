#include "libft.h"

int	ft_atoi(const char *nptr)
{
	unsigned char	*s;
	int				n;
	int				i;

	s = (unsigned char *)nptr;
	n = 0;
	i = 0;
	while (*s)
		if ((*s == 32 || (*s >= 9 && *s <= 13)) && i == 0)
			s++;
	else if (!(*s == 32 || (*s >= 9 && *s <= 13)) && i == 0)
		if ((*s == '-' && ++i && ++i) || (*s == '+' && ++i))
			s++;
	else if ((*s >= 48 && *s <= 57) && ++i)
		n = (n * 10) + (*s++ - '0');
	else
		i++;
	else if ((*s >= 48 && *s <= 57))
		n = (n * 10) + (*s++ - '0');
	else if (i != 0 && !(*s >= 48 && *s <= 57))
		break ;
	if (i == 2)
		n *= -1;
	return (n);
}
